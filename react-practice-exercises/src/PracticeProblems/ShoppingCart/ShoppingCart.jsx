import React from "react";

const ShoppingCart = () => {
  const products = [
    { id: 1, name: "Product A" },
    { id: 2, name: "Product B" },
    { id: 3, name: "Product C" },
  ];

  return (
    <div>
      <h1>ShoppingCart</h1>
      <h2>Products List</h2>
      <ul>
        {products.map((product) => (
          <li key={product.id}>{product.name}</li>
        ))}
      </ul>
    </div>
  );
};

export default ShoppingCart;
